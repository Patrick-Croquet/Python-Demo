# Exemple d'utilisation de fonctions de conversion en Python
nombre_texte = "24"
nombre_entier = int(nombre_texte)  # Fonction int() pour convertir en entier
nombre_flottant = float(nombre_texte)  # Fonction float() pour convertir en flottant

print("Nombre entier:", nombre_entier)  # Output: 24
print("Nombre flottant:", nombre_flottant)  # Output: 24.0