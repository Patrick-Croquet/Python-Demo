# Exemple d'utilisation de fonctions num√©riques en Python
nombre = 4.499
arrondi = round(nombre)  # Fonction round() pour arrondir un nombre
maximum = max(3, 7, 2, 8)  # Fonction max() pour obtenir la valeur maximale parmi les arguments
minimum = min(round(nombre), 7, 5, 8)  # Fonction min() pour obtenir la valeur minimale parmi les arguments

print("Arrondi :", arrondi)  # Output: 4
print("Maximum :", maximum)   # Output: 8
print("Minimum :", minimum)   # Output: 4
