# Déclaration d'un tableau à 2 dimensions (3x3)
tableau_2d = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

# Accès à un élément du tableau
print(tableau_2d[0][0])  # Affiche le premier élément de la première ligne (1)
print(tableau_2d[1][2])  # Affiche le troisième élément de la deuxième ligne (6)
print(tableau_2d[2][1])